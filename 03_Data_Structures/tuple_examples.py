"""
Interview Question:
What is a tuple in Python?

Answer:
A tuple is a collection of ordered elements.
Tuples are immutable, meaning their values cannot be changed.
"""

# Creating a tuple
numbers = (10, 20, 30)

print("Tuple:", numbers)

# Accessing element
print("First element:", numbers[0])

# Looping
for i in numbers:
    print(i)
