# if_else.py

# Example 1: Basic if-else
num = 10

if num > 0:
    print("Number is positive")
else:
    print("Number is negative")

# Example 2: if-elif-else
marks = 75

if marks >= 90:
    print("Grade A")
elif marks >= 70:
    print("Grade B")
elif marks >= 50:
    print("Grade C")
else:
    print("Fail")

# Example 3: Even or Odd
number = 7

if number % 2 == 0:
    print("Even number")
else:
    print("Odd number")
